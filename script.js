function updateNepaliDate() {
    const now = new Date();
    const days = ['आइतबार', 'सोमबार', 'मंगलबार', 'बुधबार', 'बिहिबार', 'शुक्रबार', 'शनिबार'];
    const day = days[now.getDay()];
    
    // Approximate Nepali date ( Bikram Sambat is roughly 56 years 8 months ahead)
    const nepaliYear = now.getFullYear() + 56;
    const months = ['बैशाख', 'जेठ', 'असार', 'श्रावण', 'भदौ', 'असोज', 'कार्तिक', 'मंसिर', 'पुष', 'माघ', 'फाल्गुन', 'चैत'];
    const month = months[now.getMonth()];
    const date = now.getDate();
    
    document.getElementById('nepaliDate').textContent = `${nepaliYear} ${month} ${date}, ${day}`;
}

